namespace Micky5991.Samp.Net.Framework.Enums
{
    /// <summary>
    /// Alignment variations for textdraws.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// No alignment has been defined.
        /// </summary>
        Default = 0,

        /// <summary>
        /// Text will be aligned on the left.
        /// </summary>
        Left = 1,

        /// <summary>
        /// Text will be centered.
        /// </summary>
        Centered = 2,

        /// <summary>
        /// Text will be aligned on the right.
        /// </summary>
        Right = 3,
    }
}
